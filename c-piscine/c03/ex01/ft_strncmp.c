int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n == 0)
		return (0);
	while (n != 0 && *s1 && *s2 && *s1 == *s2)
	{
		n--;
		s1++;
		s2++;
	}
	if (n == 0 || (!*s1 && !*s2))
		return (0);
	else if (*s1 > *s2)
		return (1);
	else
		return (-1);
}
