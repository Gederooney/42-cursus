#include <unistd.h>

int	main(int n, char *argv[])
{
	while (n > 1)
	{
		while (*argv[n - 1])
		{
			write(1, argv[n - 1], 1);
			argv[n - 1]++;
		}
		n--;
		write(1, "\n", 1);
	}
	return (0);
}
