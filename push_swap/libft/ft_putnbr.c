#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0 && n / 10 == 0)
		ft_putchar('-');
	if (n / 10 != 0)
		ft_putnbr(n / 10);
	if (n % 10 < 0)
		ft_putchar(-(n % 10) + '0');
	else
		ft_putchar((n % 10) + '0');
}
